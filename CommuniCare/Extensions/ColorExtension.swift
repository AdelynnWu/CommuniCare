import Foundation
import SwiftUI

extension Color {
    public static var matchaGreen: Color {
        return Color(UIColor(red: 123/255, green: 167/255, blue: 105/255, alpha: 1.0))
    }
    
    public static var darkGreen: Color {
        return Color(UIColor(red: 28/255, green: 51/255, blue: 18/255, alpha: 1.0))
    }
    
    public static var offwhite: Color {
        return Color(UIColor(red: 251/255, green: 247/255, blue: 245/255, alpha: 1.0))
    }
}
